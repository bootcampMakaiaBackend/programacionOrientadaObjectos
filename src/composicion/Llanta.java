package composicion;

public class Llanta {
    private int numeroRin;
    private String marca;

    public Llanta(int numeroRin, String marca) {
        this.numeroRin = numeroRin;
        this.marca = marca;
    }

    public int getNumeroRin() {
        return numeroRin;
    }

    public String getMarca() {
        return marca;
    }
}
